package hotelChains.beans;

public class Manages {
	
	private int m_sin;
	private int e_sin;

	public Manages(int m_sin, int e_sin) {
		
		this.m_sin = m_sin;
		this.e_sin = e_sin;
	}
	
	
	public int getM_sin() {
		return m_sin;
	}
	public void setM_sin(int m_sin) {
		this.m_sin = m_sin;
	}
	public int getE_sin() {
		return e_sin;
	}
	public void setE_sin(int e_sin) {
		this.e_sin = e_sin;
	}
	
	

}
